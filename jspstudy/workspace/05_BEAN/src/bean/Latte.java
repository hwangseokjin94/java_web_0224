package bean;

public class Latte {

	//필드
	private String coffee;
	private int milk;
	private int water;
	
	//생성자
	public Latte() {
		
	}

	public String getCoffee() {
		return coffee;
	}

	public void setCoffee(String coffee) {
		this.coffee = coffee;
	}

	public int getMilk() {
		return milk;
	}

	public void setMilk(int milk) {
		this.milk = milk;
	}

	public int getWater() {
		return water;
	}

	public void setWater(int water) {
		this.water = water;
	}
	
	
	
	
}
