DROP TABLE REPLY;
DROP TABLE BBS;


CREATE TABLE BBS(
	BNO NUMBER PRIMARY KEY,
	BWRITER VARCHAR2(100) NOT NULL,
	BTITLE VARCHAR2(100) NOT  NULL,
	BCONTENT VARCHAR2(1000),
	BPW VARCHAR2(100) NOT NULL,
	BHIT NUMBER,
	BIP VARCHAR2(15),
	BFILENAME VARCHAR2(300),
	BREGDATE DATE 
);


CREATE TABLE REPLY(
	RNO NUMBER PRIMARY KEY,
	RWRITER VARCHAR2(100) NOT NULL,
	RCONTENT VARCHAR2(1000),
	RPW VARCHAR2(100) NOT NULL,
	RIP VARCHAR2(15),
	RREGDATE DATE,
	BNO NUMBER NOT NULL,
	CONSTRAINT REPLY_BBS_FK FOREIGN KEY (BNO) REFERENCES BBS (BNO)
);

DROP SEQUENCE BBS_SEQ;
CREATE SEQUENCE BBS_SEQ START WITH 1 INCREMENT BY 1 MAXVALUE 999999 NOCYCLE NOCACHE;

